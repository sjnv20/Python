# < 그리디 알고리즘, 회의실 배정 문제 >

import sys

input = sys.stdin.readline

N = int(input())
events = []
for _ in range(N):
    s, e = map(int, input().split())
    events.append([s, e])

events.sort(key=lambda x: (x[1], x[0]))
count = end = 0
for s, e in events:
    if s > end:
        count += 1
        end = e

print(count)